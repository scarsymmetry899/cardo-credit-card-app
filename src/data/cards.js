export function detectCategory(t) {
  const l = t.toLowerCase();
  for (const [c, kw] of Object.entries(CAT_KW)) if (kw.some(k => l.includes(k))) return c;
  return "default";
}
export function getCardRankings(ids, cat, amt) {
  return ids.map(id => {
    const c = CARD_DB[id]; if (!c) return null;
    const r = c.benefits[cat]||c.benefits.default||1;
    return {card:c, rate:r, value:parseFloat(((amt*r)/100).toFixed(2))};
  }).filter(Boolean).sort((a,b)=>b.value-a.value);
}
