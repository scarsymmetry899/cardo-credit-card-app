export function parseSMSQuick(sms) {
  const amtM = sms.match(/(?:Rs\.?|INR|₹)\s*([0-9,]+(?:\.\d{1,2})?)/i);
  if (!amtM) return null;
  const amount = parseFloat(amtM[1].replace(/,/g,""));
  const mM = sms.match(/(?:at|to)\s+([A-Za-z0-9\s\.]+?)(?:\.|on\s)/i) || sms.match(/Info:\s*(?:UPI\/)?([A-Za-z0-9@\s]+?)(?:;|\.|Avl)/i);
  return { amount, merchant: mM ? mM[1].trim() : "Unknown Merchant" };
}
